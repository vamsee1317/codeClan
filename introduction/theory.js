// ----------------------------------------------------------
// What is JavaScript?
// ----------------------------------------------------------
// JavaScript is a lightweight, interpreted scripting language
// used mainly to make web pages interactive.
// It runs inside the browser and can update the UI dynamically.


// ----------------------------------------------------------
// History of JavaScript
// ----------------------------------------------------------
// Created by Brendan Eich in 1995 for Netscape.
// Completed in just 10 days.
// Originally called Mocha → LiveScript → JavaScript.
// Designed to add interactivity to web pages.


// ----------------------------------------------------------
// Features of JavaScript
// ----------------------------------------------------------
// 1. Lightweight
// 2. Interpreted language
// 3. Object-Oriented
// 4. Event-driven
// 5. Versatile (client + server side)
// 6. Cross-platform


